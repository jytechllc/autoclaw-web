#!/usr/bin/env node
// Extract thinking-framework overlays from alchaincyf/nuwa-skill (MIT) distillation
// SKILL.md files and emit app/api/chat/personas.generated.ts.
//
// We keep ONLY the framework/voice sections (KEEP list) and drop the roleplay /
// agentic-protocol sections. AutoClaw's chat assistant must keep its tools, RAG and
// guardrails, so it adopts a persona's thinking style and voice as an OVERLAY — it
// does not turn into a standalone roleplay agent.
//
// Usage: NUWA_DIR=~/nuwa-skill node scripts/extract-personas.mjs

import { readFileSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { join } from "node:path";

const NUWA_DIR = process.env.NUWA_DIR || join(homedir(), "nuwa-skill");

const PERSONAS = [
  { id: "munger", dir: "munger-perspective", name: "Charlie Munger", emoji: "🧠", tagline: "Multidisciplinary mental models, inversion, brutal clarity" },
  { id: "feynman", dir: "feynman-perspective", name: "Richard Feynman", emoji: "🔬", tagline: "First principles, explain it simply, intellectual honesty" },
  { id: "naval", dir: "naval-perspective", name: "Naval Ravikant", emoji: "🚀", tagline: "Leverage, specific knowledge, wealth without luck" },
  { id: "paul-graham", dir: "paul-graham-perspective", name: "Paul Graham", emoji: "📝", tagline: "Make something people want, do things that don't scale" },
  { id: "steve-jobs", dir: "steve-jobs-perspective", name: "Steve Jobs", emoji: "🍎", tagline: "Product taste, ruthless focus, story over spec" },
  { id: "zhang-yiming", dir: "zhang-yiming-perspective", name: "Zhang Yiming", emoji: "📈", tagline: "Global efficiency, data-driven growth, delayed gratification" },
  { id: "mrbeast", dir: "mrbeast-perspective", name: "MrBeast", emoji: "🎬", tagline: "Viral attention, retention obsession, reinvest everything" },
  { id: "taleb", dir: "taleb-perspective", name: "Nassim Taleb", emoji: "🦢", tagline: "Antifragility, tail risk, skin in the game" },
];

// ## headings whose blocks we keep (matched by substring on the heading text).
const KEEP = ["核心心智模型", "决策启发式", "表达DNA", "内容创造公式", "价值观与反模式", "诚实边界"];

function extractOverlay(md) {
  const lines = md.split("\n");
  const blocks = [];
  let cur = null;
  for (const line of lines) {
    const m = /^##\s+(.*)$/.exec(line);
    if (m) {
      if (cur) blocks.push(cur);
      cur = { title: m[1].trim(), body: [] };
    } else if (cur) {
      cur.body.push(line);
    }
  }
  if (cur) blocks.push(cur);
  const kept = blocks.filter((b) => KEEP.some((k) => b.title.includes(k)));
  return kept.map((b) => `## ${b.title}\n${b.body.join("\n").trim()}`).join("\n\n").trim();
}

const out = [];
for (const p of PERSONAS) {
  const path = join(NUWA_DIR, "examples", p.dir, "SKILL.md");
  const md = readFileSync(path, "utf8");
  const overlay = extractOverlay(md);
  if (!overlay) throw new Error(`No whitelisted sections extracted for ${p.id} (${path})`);
  out.push({ id: p.id, name: p.name, emoji: p.emoji, tagline: p.tagline, source: `alchaincyf/nuwa-skill examples/${p.dir}/SKILL.md (MIT)`, overlay });
  console.error(`${p.id.padEnd(14)} ${overlay.length} chars`);
}

const file = `// AUTO-GENERATED by scripts/extract-personas.mjs — DO NOT EDIT BY HAND.
// Persona overlays distilled from alchaincyf/nuwa-skill (MIT License,
// https://github.com/alchaincyf/nuwa-skill). Only the thinking-framework and voice
// sections are kept; roleplay / agentic-protocol sections are dropped so the AutoClaw
// assistant adopts the style as an OVERLAY while keeping its tools, RAG and guardrails.
// Regenerate with: NUWA_DIR=~/nuwa-skill node scripts/extract-personas.mjs

export interface GeneratedPersona {
  id: string;
  name: string;
  emoji: string;
  tagline: string;
  source: string;
  overlay: string;
}

export const GENERATED_PERSONAS: GeneratedPersona[] = ${JSON.stringify(out, null, 2)};
`;

writeFileSync(join(process.cwd(), "app/api/chat/personas.generated.ts"), file);
console.error(`\nWrote ${out.length} personas to app/api/chat/personas.generated.ts`);
